package com.java.problem3.operations;

public class ScientificCalculator {

    public static double sqrt(double x){
        return Math.sqrt(x);
    }

    public static double logOf(int x){
        return Math.log(x);
    }

    public static double logn(int x){
        return Math.log10(x);
    }
    public static double Sin(double x){
        return Math.sin(x);
    }
    public static double iSin(double x){
        return Math.asin(x);
    }
    public static double Cos(double x){
        return Math.cos(x);
    }
    public static double iCos(double x){
        return Math.acos(x);
    }
    public static double Tan(double x){
        return Math.tan(x);
    }
    public static double iTan(double x){
        return Math.atan(x);
    }
}

